    public void cadastrarPessoa(String nome, String cpf, String data, String mc){
        cont = true;
        for(pessoa p:form3.Validar()){
             if(p.getCpf().equals(cpf)){
                 cont = false;
                 break;
             }else{
                 form2.setId(p.getId()+1);
             }
        }
        if(cont == true){
            form2.setNome(nome);
            if(data.length()<=8){
                data = aux.dataBrasil(data);
                form2.setData(aux.dataBD(data));

            }
            else{
                form2.setData(aux.dataBD(data));
            }
            if(cpf.length()<=11){
                form2.setCpf(aux.CpfConvertido(cpf));            
            }else if(cpf.length()==14)
                form2.setCpf(cpf);
            else
                JOptionPane.showMessageDialog(null,"CPF INVÁLIDO");
            if(btnSim.isSelected())
                form2.setMc(mc+"-"+boxEstado.getSelectedItem().toString());
            else if(btnNao.isSelected())
                form2.setMc("");
        }
        else{
            JOptionPane.showMessageDialog(null,"ERRO AO CADASTRAR USUÁRIO! JÁ EXISTE UM CPF CADASTRADO PARA ESSA PESSOA.");
        }
        //return cont;
    }
    
    public void cadastrarUsuario(String usuario){
        cont = true;
        for(usuario u:form1.Validar()){
             if(u.getNome().equals(usuario)){
                 cont = false;
                 break;
             }else{
                 form.setIduser(u.getIduser()+1);
                 form.setIdpessoa(form2.getId());
             }
        }
        if(cont == true){
            form.setNome(usuario);
            senha = txtSenha.getText();
            confirmar = txtConf.getText();
            
            if(senha.equals(confirmar) != true){
                JOptionPane.showMessageDialog(null,"SENHAS INVÁLIDAS!");               
                cont = false;
            }
            else if(senha.length()< 8 || confirmar.length() < 8){
                JOptionPane.showMessageDialog(null,"QUANTIDADE MÍN. DE 8 CARACTERES E MÁX. DE 12 CARACTERES.");               
                cont = false;
            }
            else{
                form.setSenha(confirmar);
            }
        }else{
            JOptionPane.showMessageDialog(null,"ERRO AO SALVAR!JÁ EXISTE UM USUÁRIO COM ESSE NOME.");
        }
    }
    
