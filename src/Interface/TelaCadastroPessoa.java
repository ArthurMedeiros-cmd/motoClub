    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if(txtNome.getText().trim().equals("") || txtUsuario.getText().trim().equals("") || txtCpf.getText().trim().equals("") || txtData.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"CAMPOS OBRIGATÓRIOS!");
        }
            nome = txtNome.getText();
            usuario = txtUsuario.getText();
            cpf = txtCpf.getText();
            data = txtData.getText();
            mc = txtMC.getText();
            mc = mc.substring(0,1).toUpperCase()+mc.substring(1);
            for(motoClub mc:form5.Validar()){
               if(mc.getNome().equals(this.mc)){
                   form.setIdmc(mc.getId());
                   //AQUI
                   break;
               }
            }
            cadastrarPessoa(nome, cpf, data, mc);        
            cadastrarUsuario(usuario);
            if(cont == true){
                if(form3.Cadastrar(form2)){
                    if(form1.cadastrar(form)){
                        JOptionPane.showMessageDialog(null,"SALVO COM SUCESSO!");
                    }else{
                        return;
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"ERRO AO SALVAR! DADOS INVÁLIDOS!");
                    return;
                }  
                login.getUsuario(form);
                login.getPessoa(form2);
                login.setVisible(true);
                dispose();
            }
    }//GEN-LAST:event_btnCadastrarActionPerformed

