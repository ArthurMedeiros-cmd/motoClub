    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if(txtNome.getText().trim().equals("") || txtQtd.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"CAMPOS OBRIGATÓRIOS!");
        }
        nome = txtNome.getText();
        regiao = boxRegião.getSelectedItem().toString();
        estado = boxEstado.getSelectedItem().toString();
        qtd = Integer.parseInt(txtQtd.getText());
        cadastrarMC(nome, regiao, estado, qtd);
        if(cont == true){
           if(form.Cadastrar(mc)){
                JOptionPane.showMessageDialog(null,"SALVO COM SUCESSO!");
            }else{
                JOptionPane.showMessageDialog(null,"ERRO AO SALVAR! DADOS INVÁLIDOS!");
            }
            TelaMenuInicial menu = new TelaMenuInicial();
            menu.getMc(mc);
            menu.setVisible(true);
            dispose();   
        }
            
    }//GEN-LAST:event_btnCadastrarActionPerformed

