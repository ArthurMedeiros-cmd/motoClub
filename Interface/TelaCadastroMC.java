    public void cadastrarMC (String nome, String regiao, String estado, int qtd){
        cont = true;
        for(motoClub mc:form.Validar()){
             if(mc.getNome().equals(nome)){
                 if(mc.getRegiao().equals(regiao))
                     if(mc.getEstado().equals(estado)){
                        cont = false;
                        break;
                     }
             }else{
                    System.out.println(mc.getId()+1);
                    this.mc.setId(mc.getId()+1);
                }
        }
        if(cont == true){
            mc.setNome(nome);
            mc.setRegiao(regiao);
            mc.setEstado(estado);
            mc.setQtd(qtd);
        }else{
            JOptionPane.showMessageDialog(null,"ERRO AO CADASTRAR MOTO CLUB! JÁ EXISTE UM MC CADASTRADO PARA ESSA REGIÃO E ESTADO.");
        }
        
    }
    
