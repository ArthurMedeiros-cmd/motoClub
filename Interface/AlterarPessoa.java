    public void removerIntegrantes(usuario u){
        System.out.println(u.getIdmc());
        for(motoClub mc:motoClubs){
            if(u.getIdmc() == mc.getId()){
                mc.setIntegrantes(mc.getIntegrantes()-1);
                form2.CadastrarIntegrante(mc);
            }
            System.out.println("nome: "+mc.getNome()+" int: "+mc.getIntegrantes());
        }
    }
    public void adicionarNovo(usuario u){
        System.out.println(u.getIdmc());
        for(motoClub mc:motoClubs){
            if(u.getIdmc() == mc.getId()){
                mc.setIntegrantes(mc.getIntegrantes()+1);
                form2.CadastrarIntegrante(mc);
            }
            System.out.println("nome: "+mc.getNome()+" int: "+mc.getIntegrantes());
        }
            
    }
    
