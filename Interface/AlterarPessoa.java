    private void btnConfimarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfimarActionPerformed
        // TODO add your handling code here:
        if(checkNome.isSelected() == true){
                pessoa.setNome(txtCampo.getText());
                if(form1.Alterar(pessoa, checkNome.getText())){
                    JOptionPane.showMessageDialog(null,"Alterado com sucesso!");
                    dispose();
                }else
                    JOptionPane.showMessageDialog(null, "Erro ao alterar! Informação incorreta.");
        }else if(checkCpf.isSelected() == true){
             
                if(txtCampo.getText().length()<=11){
                    pessoa.setCpf(aux.CpfConvertido(txtCampo.getText()));            
                }else if(txtCampo.getText().length()==14)
                    pessoa.setCpf(txtCampo.getText());
                else
                     JOptionPane.showMessageDialog(null,"CPF INVÁLIDO");
            if(form1.Alterar(pessoa, checkCpf.getText())){
                JOptionPane.showMessageDialog(null,"Alterado com sucesso!");
                dispose();
            }else
                JOptionPane.showMessageDialog(null, "Erro ao alterar! Informação incorreta.");
        }else if(checkData.isSelected() == true){
               if(txtCampo.getText().length()<=8){
                   // String data = aux.dataBrasil(txtCampo.getText());
                    pessoa.setData(aux.dataBD(txtCampo.getText()));

                }
                else{
                    pessoa.setData(aux.dataBD(txtCampo.getText()));
                }
            if(form1.Alterar(pessoa, checkData.getText())){
               JOptionPane.showMessageDialog(null,"Alterado com sucesso!");
               dispose();
           }else
               JOptionPane.showMessageDialog(null, "Erro ao alterar! Informação incorreta.");
        }else if(checkMC.isSelected() == true){
            String campo = txtCampo.getText();
            campo = campo.substring(0,1).toUpperCase()+campo.substring(1);
            boolean cont = false;
            for(motoClub mc:form2.Validar()){
                if(mc.getNome().indexOf(campo) != -1){
                    removerIntegrantes(usuario);
                    pessoa.setMc(mc.getNome()+"-"+mc.getEstado());
                    usuario.setIdmc(mc.getId());
                    this.mc = mc;
                    cont = true;
                    break;
                }
            }   
            if(cont){
                if(form1.Alterar(pessoa, checkMC.getText())){
                   if(form.Alterar(usuario,checkMC.getText())){
                        JOptionPane.showMessageDialog(null,"Alterado com sucesso!");
                        adicionarNovo(usuario);
                        dispose();
                   }else
                        JOptionPane.showMessageDialog(null, "Erro ao alterar! Informação incorreta.");
                }else
                        JOptionPane.showMessageDialog(null, "Erro ao alterar! Informação incorreta.");
            }else
                JOptionPane.showMessageDialog(null,"MOTOCLUB NÃO CADASTRADO, CASO QUEIRA CADASTRAR O SEU MC VÁ AO MENU INICIAL E CLIQUE NO LINL CADASTRAR.");
        }else
            JOptionPane.showMessageDialog(null,"Selecione uma Opção.");
    }//GEN-LAST:event_btnConfimarActionPerformed

